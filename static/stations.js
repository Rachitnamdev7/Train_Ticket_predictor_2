function setupAutocomplete(inputId, suggestionsId) {
  const input = document.getElementById(inputId);
  const suggestions = document.getElementById(suggestionsId);

  input.addEventListener("input", function () {
    const inputVal = input.value.toLowerCase();
    const filtered = Object.keys(stationList).filter(station => station.toLowerCase().includes(inputVal));
    suggestions.innerHTML = "";

    if (inputVal && filtered.length) {
      filtered.forEach(station => {
        const div = document.createElement("div");
        div.textContent = `${station} - ${stationList[station]}`;
        div.onclick = () => {
          input.value = station;  // set only code
          suggestions.innerHTML = "";
        };
        suggestions.appendChild(div);
      });
    }
  });

  document.addEventListener("click", function (e) {
    if (!input.contains(e.target)) {
      suggestions.innerHTML = "";
    }
  });
}

document.addEventListener("DOMContentLoaded", function () {
  setupAutocomplete("source", "sourceSuggestions");
  setupAutocomplete("destination", "destinationSuggestions");
});
